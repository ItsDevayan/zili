/**
 * context-generator.js
 * Auto-generates CONTEXT.md and TASKS.md for each project.
 * These files are designed to be pasted directly into any AI coding assistant.
 */

const fs = require('fs');
const path = require('path');

// ─── CONTEXT.md ───────────────────────────────────────────────────────────────

function generateContextMd(project) {
  const now = new Date().toISOString().replace('T', ' ').slice(0, 16) + ' UTC';

  const sections = [
    `# ${project.name} — AI Context Document`,
    ``,
    `> 🤖 Auto-generated by [ideas-workflow](https://github.com/devayan/ideas-workflow).`,
    `> Paste this file into any AI coding assistant (Claude, Copilot, Cursor, etc.) for instant project context.`,
    `> Last updated: ${now}`,
    ``,
    `---`,
    ``,
    `## What is this project?`,
    ``,
    project.context || '_No project context set. Add a "context" field in config.json._',
    ``,
    `---`,
    ``,
  ];

  // Active tasks
  const activeTasks = readActiveTaskSummaries(project.activeTasksDir);
  sections.push(`## Active Tasks (${activeTasks.length})`);
  sections.push('');
  if (activeTasks.length === 0) {
    sections.push('_No active tasks yet._');
  } else {
    activeTasks.forEach((t) => {
      sections.push(`### ${t.title}`);
      sections.push(`- **Complexity**: ${t.complexity} | **Priority**: ${t.priority}`);
      sections.push(`- **Description**: ${t.description.split('\n')[0].slice(0, 200)}`);
      if (t.acceptance_criteria?.length) {
        sections.push(`- **Done when**:`);
        t.acceptance_criteria.forEach((c) => sections.push(`  - ${c}`));
      }
      sections.push('');
    });
  }
  sections.push('---', '');

  // Recent polished ideas
  const recentIdeas = readRecentPolishedIdeas(project.polishedDir, 5);
  sections.push(`## Recently Polished Ideas`);
  sections.push('');
  if (recentIdeas.length === 0) {
    sections.push('_No polished ideas yet._');
  } else {
    recentIdeas.forEach((idea) => {
      sections.push(`- **${idea.title}** — ${idea.reason}`);
    });
  }
  sections.push('', '---', '');

  // AI prompt starter
  sections.push('## Quick AI Prompt Starter');
  sections.push('');
  sections.push('> Copy-paste this when starting a new AI session:');
  sections.push('');
  sections.push('```');
  sections.push(
    `You are helping me work on "${project.name}". Here is the project context:\n\n${project.context}\n\nActive tasks:\n${activeTasks.map((t) => `- ${t.title} (${t.complexity}/${t.priority})`).join('\n') || 'None yet.'}\n\nPlease help me with: [YOUR REQUEST HERE]`,
  );
  sections.push('```');
  sections.push('');

  return sections.join('\n');
}

// ─── TASKS.md ─────────────────────────────────────────────────────────────────

function generateTasksMd(project) {
  const now = new Date().toISOString().replace('T', ' ').slice(0, 16) + ' UTC';
  const activeTasks = readActiveTaskSummaries(project.activeTasksDir);
  const doneTasks = readDoneTaskSummaries(project.doneTasksDir);

  const lines = [
    `# ${project.name} — Task Board`,
    ``,
    `> Auto-generated by ideas-workflow | Last updated: ${now}`,
    `> Move a task folder from \`active_tasks/\` → \`done_tasks/\` to mark it done.`,
    ``,
    `---`,
    ``,
    `## 🔥 Active (${activeTasks.length})`,
    ``,
  ];

  if (activeTasks.length === 0) {
    lines.push('_Nothing active. Drop a .txt into new_tasks/ to create one._');
    lines.push('');
  } else {
    // Group by priority
    const priorities = ['critical', 'high', 'medium', 'low'];
    priorities.forEach((p) => {
      const group = activeTasks.filter((t) => t.priority === p);
      if (group.length === 0) return;
      lines.push(`### ${p.charAt(0).toUpperCase() + p.slice(1)}`);
      group.forEach((t) => {
        lines.push(`- [ ] **${t.title}** \`${t.complexity}\` — ${t.description.split('\n')[0].slice(0, 120)}`);
        lines.push(`  > _Task file: \`active_tasks/${t.id}/\`_`);
      });
      lines.push('');
    });
  }

  lines.push('---', '', `## ✅ Done (${doneTasks.length})`, '');
  if (doneTasks.length === 0) {
    lines.push('_No completed tasks yet._');
  } else {
    doneTasks.slice(0, 10).forEach((t) => {
      lines.push(`- [x] **${t.title}** \`${t.complexity}\``);
    });
    if (doneTasks.length > 10) lines.push(`_...and ${doneTasks.length - 10} more_`);
  }

  return lines.join('\n');
}

// ─── Global TASKS.md (across all projects) ────────────────────────────────────

function generateGlobalTasksMd(projects) {
  const now = new Date().toISOString().replace('T', ' ').slice(0, 16) + ' UTC';
  const lines = [
    `# Global Task Overview`,
    ``,
    `> Auto-generated by ideas-workflow | Last updated: ${now}`,
    ``,
    `---`,
    ``,
  ];

  let totalActive = 0;
  projects.forEach((project) => {
    const tasks = readActiveTaskSummaries(project.activeTasksDir);
    totalActive += tasks.length;

    lines.push(`## ${project.name} (${tasks.length} active)`);
    lines.push('');
    if (tasks.length === 0) {
      lines.push('_No active tasks._');
    } else {
      tasks.forEach((t) => {
        lines.push(
          `- [ ] **${t.title}** \`${t.complexity}\` \`${t.priority}\``,
        );
      });
    }
    lines.push('');
  });

  lines.unshift(`> **Total active tasks across all projects: ${totalActive}**`, '');

  return lines.join('\n');
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

function readActiveTaskSummaries(activeTasksDir) {
  if (!fs.existsSync(activeTasksDir)) return [];

  return fs
    .readdirSync(activeTasksDir)
    .filter((name) => {
      const metaPath = path.join(activeTasksDir, name, 'meta.json');
      return fs.existsSync(metaPath);
    })
    .map((name) => {
      const meta = JSON.parse(
        fs.readFileSync(path.join(activeTasksDir, name, 'meta.json'), 'utf-8'),
      );
      return { id: name, ...meta };
    })
    .sort((a, b) => {
      const priorityOrder = { critical: 0, high: 1, medium: 2, low: 3 };
      return (priorityOrder[a.priority] ?? 99) - (priorityOrder[b.priority] ?? 99);
    });
}

function readDoneTaskSummaries(doneTasksDir) {
  if (!fs.existsSync(doneTasksDir)) return [];

  return fs
    .readdirSync(doneTasksDir)
    .filter((name) => fs.existsSync(path.join(doneTasksDir, name, 'meta.json')))
    .map((name) =>
      JSON.parse(fs.readFileSync(path.join(doneTasksDir, name, 'meta.json'), 'utf-8')),
    );
}

function readRecentPolishedIdeas(polishedDir, limit = 5) {
  if (!fs.existsSync(polishedDir)) return [];

  return fs
    .readdirSync(polishedDir)
    .filter((name) => fs.existsSync(path.join(polishedDir, name, 'meta.json')))
    .map((name) =>
      JSON.parse(fs.readFileSync(path.join(polishedDir, name, 'meta.json'), 'utf-8')),
    )
    .filter((m) => m.verdict === 'polish')
    .slice(-limit)
    .reverse();
}

// ─── Write files ──────────────────────────────────────────────────────────────

function updateProjectFiles(project) {
  const contextMd = generateContextMd(project);
  const tasksMd = generateTasksMd(project);

  fs.writeFileSync(project.contextFile, contextMd, 'utf-8');
  fs.writeFileSync(project.tasksFile, tasksMd, 'utf-8');
}

function updateGlobalTasksFile(projects, globalTasksPath) {
  const globalMd = generateGlobalTasksMd(projects);
  fs.writeFileSync(globalTasksPath, globalMd, 'utf-8');
}

module.exports = { updateProjectFiles, updateGlobalTasksFile };
